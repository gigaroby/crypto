func EditDistance(d1, d2 []byte) (int, error) {
	if len(d1) != len(d2) {
		return 0, errors.New("data to compare must have the same length")
	}

	d := 0
	for i, b := range d1 {
		d += table[b^d2[i]]
	}

	return d, nil
}
